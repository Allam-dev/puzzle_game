import 'package:flutter/material.dart';

abstract class AppColors {
  static Color get white => Colors.white;
  static Color get lightGrey => Colors.grey[100]!;
  static Color get grey => Colors.grey[400]!;
  static Color get lightPurple => Colors.purple[50]!;
  static Color get purple => Colors.purple[300]!;
  static Color get black => Colors.black;
  static Color get yellow => Colors.yellow;
  static Color get amber => Colors.amber;
  static Color get orange => Colors.orange;
}
